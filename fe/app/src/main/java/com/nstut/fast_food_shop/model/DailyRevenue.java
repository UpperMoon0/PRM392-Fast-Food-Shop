package com.nstut.fast_food_shop.model;

public class DailyRevenue {
    private String date;
    private double total;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public double getTotal() {
        return total;
    }

    public void setTotal(double total) {
        this.total = total;
    }
}